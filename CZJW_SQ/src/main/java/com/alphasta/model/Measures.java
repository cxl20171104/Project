package com.alphasta.model;

public class Measures {
   String id;
   String meaState;
   String meaName;
   String meaTime;
   String meaResult;
   String reflectedId;
   String time;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMeaState() {
		return meaState;
	}
	public void setMeaState(String meaState) {
		this.meaState = meaState;
	}
	public String getMeaName() {
		return meaName;
	}
	public void setMeaName(String meaName) {
		this.meaName = meaName;
	}
	public String getMeaTime() {
		return meaTime;
	}
	public void setMeaTime(String meaTime) {
		this.meaTime = meaTime;
	}
	public String getMeaResult() {
		return meaResult;
	}
	public void setMeaResult(String meaResult) {
		this.meaResult = meaResult;
	}
	public String getReflectedId() {
		return reflectedId;
	}
	public void setReflectedId(String reflectedId) {
		this.reflectedId = reflectedId;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
   
   
}
