package com.alphasta.model;

public class ZyViolation {
   String id;
   String reflectedId;
   String zyName;
   String zyTime;
   String zyResult;
   String time;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getReflectedId() {
		return reflectedId;
	}
	public void setReflectedId(String reflectedId) {
		this.reflectedId = reflectedId;
	}
	public String getZyName() {
		return zyName;
	}
	public void setZyName(String zyName) {
		this.zyName = zyName;
	}
	public String getZyTime() {
		return zyTime;
	}
	public void setZyTime(String zyTime) {
		this.zyTime = zyTime;
	}
	public String getZyResult() {
		return zyResult;
	}
	public void setZyResult(String zyResult) {
		this.zyResult = zyResult;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
   
   
}
