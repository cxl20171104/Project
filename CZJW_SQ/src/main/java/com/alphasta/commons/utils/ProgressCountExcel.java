package com.alphasta.commons.utils;

public class ProgressCountExcel {
	private String xxsCount;
	private String beReflectRank;
	private String xxstate;
	private String problemLand;
	
	public String getXxsCount() {
		return xxsCount;
	}
	public void setXxsCount(String xxsCount) {
		this.xxsCount = xxsCount;
	}
	public String getBeReflectRank() {
		return beReflectRank;
	}
	public void setBeReflectRank(String beReflectRank) {
		this.beReflectRank = beReflectRank;
	}
	public String getXxstate() {
		return xxstate;
	}
	public void setXxstate(String xxstate) {
		this.xxstate = xxstate;
	}
	
	public String getProblemLand() {
		return problemLand;
	}
	public void setProblemLand(String problemLand) {
		this.problemLand = problemLand;
	}
	@Override
	public String toString() {
		return "ProgressCountExcel [xxsCount=" + xxsCount + ", beReflectRank="
				+ beReflectRank + ", xxstate=" + xxstate + ", problemLand="
				+ problemLand + "]";
	}
	
	
	
	
}
